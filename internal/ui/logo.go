package ui

import (
	"fmt"

	"github.com/fatih/color"
)

func ShowLogo() {
	fmt.Println()

	logo := `
            -------    ----------
    ------ --+@@*-------+@@@@@@@=---------  ------------ ------
 ---+*##*=---*@@+-=*#+-*@@=--+@@*-=#*#*##+=--=#*=--=*#=--=*###+--
--+@@%*#@@%-=%@%+#@@*=------=@@%--%@@%#%@@@=-#@@=--#@@+-%@@*+%@@--
-=@@#--=%@@=+@@@@@#--- ---+@@@+---@@%---*@@+=@@%--=%@%==%@@%#*=---
-*@@+--=@@%-*@@%@@@=---=#@@#=----+@@#--=%@@=+@@*--+@@#---++*%@@#-
-=@@@#%@@#-=%@%==@@@+-#@@@@@@@@=-%@@@%%@@@=-+@@@@%@@@+-%@@##@@@=-
 --=*#*+-------------------------@@%-+**=----=+****+=---=+***=--
   -----  ------ ---------------+@@+----      --------  -----
                              -------
 `

	atColor := color.New(color.FgCyan).SprintFunc()
	dimColor := color.New(color.FgHiBlack).SprintFunc()

	for _, ch := range logo {
		switch ch {
		case '@', '#', '%', '*':
			fmt.Print(atColor(string(ch)))
		case '-', '=', '+':
			fmt.Print(dimColor(string(ch)))
		default:
			fmt.Print(string(ch))
		}
	}
	fmt.Println()
}
